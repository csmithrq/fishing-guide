class FishProfile:

    def __init__(self
                 , fish
                 , fishGroup
                 , feedingType
                 , feedingNotes
                 , sizeByAge
                 , legality
                 , additionalNotes
                 ):
        self.fish = fish
        self.fishGroup = fishGroup
        self.feedingType = feedingType
        self.feedingNotes = feedingNotes
        self.sizeByAge = sizeByAge
        self.legality = legality
        self.additionalNotes = additionalNotes